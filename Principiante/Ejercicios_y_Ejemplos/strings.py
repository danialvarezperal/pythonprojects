myStr = "Hola Mundo"

print(myStr.upper())#Todo Mayuscuyla

print(myStr.lower())#Todo Minuscula 

print(myStr.swapcase())#Intercalando Mayuscula y Minuscula

print(myStr.capitalize())#Poniendo en Mayuscula la letra incial

print(myStr.replace("Hola","Hasta luehgo").upper())#Remplazando el texto seleccionado por otro y poniendolo en mayuscula al final

print(myStr.count("l"))#Contando las letras L

print(myStr.startswith("H"))#Verificando si la primera letra es H mayuscula (Boolean)

print(myStr.endswith("o"))#Verificando si la ultima letra es o minuscula (Boolean)

print(myStr.split(" "))#Separando el texto apartir del caracter indicado en este caso en blanco